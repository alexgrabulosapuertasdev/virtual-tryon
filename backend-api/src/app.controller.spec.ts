import { Test, TestingModule } from '@nestjs/testing';
import { AppController } from './app.controller';
import { AppService } from './app.service';

describe('AppController', () => {
  let appController: AppController;

  beforeEach(async () => {
    const app: TestingModule = await Test.createTestingModule({
      controllers: [AppController],
      providers: [AppService],
    }).compile();

    appController = app.get<AppController>(AppController);
  });

  describe('root', () => {
    it('should return "Hello World!"', () => {
      expect(appController.getHello()).toBe(
        'Welcome to the Virtual Try-On API! This API allows you to try on different products virtually using augmented reality technology. You can upload an image of yourself and select a product to see how it looks on you. Enjoy your virtual shopping experience!',
      );
    });
  });
});
